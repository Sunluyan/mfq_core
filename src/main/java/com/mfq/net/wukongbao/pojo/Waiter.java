package com.mfq.net.wukongbao.pojo;

public class Waiter {
	
	private String waiterKey;
	private String waiterName;
	private String waiterIDType;
	private String waiterIDInfo;
	private String waiterPhone;
	public String getWaiterKey() {
		return waiterKey;
	}
	public void setWaiterKey(String waiterKey) {
		this.waiterKey = waiterKey;
	}
	public String getWaiterName() {
		return waiterName;
	}
	public void setWaiterName(String waiterName) {
		this.waiterName = waiterName;
	}
	public String getWaiterIDType() {
		return waiterIDType;
	}
	public void setWaiterIDType(String waiterIDType) {
		this.waiterIDType = waiterIDType;
	}
	public String getWaiterIDInfo() {
		return waiterIDInfo;
	}
	public void setWaiterIDInfo(String waiterIDInfo) {
		this.waiterIDInfo = waiterIDInfo;
	}
	public String getWaiterPhone() {
		return waiterPhone;
	}
	public void setWaiterPhone(String waiterPhone) {
		this.waiterPhone = waiterPhone;
	}
	
}
